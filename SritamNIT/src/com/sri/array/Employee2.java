package com.sri.array;

public class Employee2 {
private int id;
private String name;
private double salary;
public Employee2(int id, String name, double salary) {
	super();
	this.id = id;
	this.name = name;
	this.salary = salary;
}
@Override
public String toString() {
	return "Employee2 [id=" + id + ", name=" + name + ", salary=" + salary + "]";
}

}
